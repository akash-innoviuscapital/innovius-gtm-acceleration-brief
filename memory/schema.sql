-- ─────────────────────────────────────────────
-- RUN_QUALITY — source health and signal counts over time
-- Mirrors run-quality-log.json but queryable
-- Powers the weekly health report
-- ─────────────────────────────────────────────
CREATE TABLE IF NOT EXISTS run_quality (
  id                        INTEGER PRIMARY KEY AUTOINCREMENT,
  date                      TEXT NOT NULL,       -- YYYY-MM-DD
  day_of_week               TEXT,
  slack_channels_calls      INTEGER DEFAULT 0,
  slack_channels_signals    INTEGER DEFAULT 0,
  slack_channels_status     TEXT DEFAULT 'ok',
  slack_search_calls        INTEGER DEFAULT 0,
  slack_search_signals      INTEGER DEFAULT 0,
  slack_search_status       TEXT DEFAULT 'ok',
  gmail_calls               INTEGER DEFAULT 0,
  gmail_signals             INTEGER DEFAULT 0,
  gmail_status              TEXT DEFAULT 'ok',
  granola_calls             INTEGER DEFAULT 0,
  granola_signals           INTEGER DEFAULT 0,
  granola_status            TEXT DEFAULT 'ok',
  cloudzero_calls           INTEGER DEFAULT 0,
  cloudzero_signals         INTEGER DEFAULT 0,
  cloudzero_status          TEXT DEFAULT 'ok',
  hot_signals               INTEGER DEFAULT 0,
  hot_actions               INTEGER DEFAULT 0,
  carry_over_total          INTEGER DEFAULT 0,
  oldest_carry_over_days    INTEGER DEFAULT 0,
  channels_silent           TEXT,                -- JSON array
  sources_failed            TEXT                 -- JSON array
);

CREATE INDEX IF NOT EXISTS idx_run_quality_date ON run_quality(date);
